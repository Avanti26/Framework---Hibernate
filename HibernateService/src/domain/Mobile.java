package domain;

public class Mobile 
{
	private int mobileId;
	private String mobileName;
	private int mobileQty;
	private double mobilePrice;
	private String mobileCategory;
	
	public int getMobileId() {
		return mobileId;
	}
	public void setMobileId(int mobileId) {
		this.mobileId = mobileId;
	}
	public String getMobileName() {
		return mobileName;
	}
	public void setMobileName(String mobileName) {
		this.mobileName = mobileName;
	}
	public int getMobileQty() {
		return mobileQty;
	}
	public void setMobileQty(int mobileQty) {
		this.mobileQty = mobileQty;
	}
	public double getMobilePrice() {
		return mobilePrice;
	}
	public void setMobilePrice(double mobilePrice) {
		this.mobilePrice = mobilePrice;
	}
	public String getMobileCategory() {
		return mobileCategory;
	}
	public void setMobileCategory(String mobileCategory) {
		this.mobileCategory = mobileCategory;
	}
	
	
}
